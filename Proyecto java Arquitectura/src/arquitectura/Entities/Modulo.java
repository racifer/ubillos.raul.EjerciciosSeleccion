package arquitectura.Entities;

public class Modulo {
	public int idModulo;
	public String DescripcionModulo;
	public String URLModulo;
	
	public int getIdModulo() {
		return idModulo;
	}
	public void setIdModulo(int idModulo) {
		this.idModulo = idModulo;
	}
	public String getDescripcionModulo() {
		return DescripcionModulo;
	}
	public void setDescripcionModulo(String descripcionModulo) {
		DescripcionModulo = descripcionModulo;
	}
	public String getURLModulo() {
		return URLModulo;
	}
	public void setURLModulo(String uRLModulo) {
		URLModulo = uRLModulo;
	}
}
